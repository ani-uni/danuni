/**
 * 常见的加密哈希函数代号枚举。
 * 按照不同的安全等级和输出长度进行分类。
 */
export enum HashAlgorithm {
  // --- 已知不安全或不推荐用于新项目的算法 ---
  /**
   * MD5 (Message-Digest Algorithm 5) - 128位。
   * 已发现严重碰撞缺陷，不安全，不应用于安全性要求高的场景。
   * 仍可能用于非安全性的文件校验。
   */
  MD5 = 'MD5',

  /**
   * SHA-1 (Secure Hash Algorithm 1) - 160位。
   * 已发现理论上和实践中的碰撞攻击，不安全，不应用于安全性要求高的场景。
   * 仍存在于旧系统或非关键应用。
   */
  // SHA1 = 'SHA1',
  SHA_1 = 'SHA-1', // 常见别名

  // --- 当前安全且广泛推荐使用的算法 (SHA-2 系列) ---
  /**
   * SHA-224 - 224位。SHA-2家族成员，比SHA-256稍短。
   */
  // SHA224 = 'SHA224',
  SHA_224 = 'SHA-224',

  /**
   * SHA-256 - 256位。SHA-2家族最常用成员，广泛用于区块链、数字签名等。
   */
  // SHA256 = 'SHA256',
  SHA_256 = 'SHA-256',

  /**
   * SHA-384 - 384位。SHA-2家族成员，通常基于SHA-512的截断版本。
   */
  // SHA384 = 'SHA384',
  SHA_384 = 'SHA-384',

  /**
   * SHA-512 - 512位。SHA-2家族成员，提供更高安全性和更长哈希值。
   */
  // SHA512 = 'SHA512',
  SHA_512 = 'SHA-512',

  /**
   * SHA-512/224 - 224位。基于SHA-512内部结构，截断输出至224位。
   */
  // SHA512_224 = 'SHA512/224',
  SHA_512_224 = 'SHA-512/224', // 常见别名

  /**
   * SHA-512/256 - 256位。基于SHA-512内部结构，截断输出至256位。
   */
  // SHA512_256 = 'SHA512/256',
  SHA_512_256 = 'SHA-512/256', // 常见别名

  // --- 新一代加密哈希函数 (SHA-3 系列) ---
  /**
   * SHA3-224 - 224位。SHA-3家族成员，基于Keccak。
   */
  SHA3_224 = 'SHA3-224',

  /**
   * SHA3-256 - 256位。SHA-3家族常用成员，基于Keccak。
   */
  SHA3_256 = 'SHA3-256',

  /**
   * SHA3-384 - 384位。SHA-3家族成员，基于Keccak。
   */
  SHA3_384 = 'SHA3-384',

  /**
   * SHA3-512 - 512位。SHA-3家族成员，基于Keccak。
   */
  SHA3_512 = 'SHA3-512',

  // --- 其他高性能且安全的加密哈希函数 ---
  /**
   * BLAKE2b - 可变输出（最高512位）。针对64位系统优化的高性能加密哈希。
   */
  BLAKE2b = 'BLAKE2b',

  /**
   * BLAKE2s - 可变输出（最高256位）。针对32位系统优化的高性能加密哈希。
   */
  BLAKE2s = 'BLAKE2s',

  /**
   * BLAKE3 - 可变输出（通常256位）。极高性能，支持并行化，是BLAKE2的下一代。
   */
  BLAKE3 = 'BLAKE3',
}
